public class Alkohol {

    double alkoholgehalt;
    double sip;

    public Alkohol(double alkoholgehalt, double sip) {
        this.alkoholgehalt = alkoholgehalt;
    }

    protected double getAlkoholgehalt() {
        return alkoholgehalt;
    }

    protected double sipsip() {
        return sip;
    }

}